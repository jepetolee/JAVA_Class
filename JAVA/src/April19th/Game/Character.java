package April19th.Game;

public class Character {
    int x,y;
    public Character(){

    }
    public void move(java.lang.Character character){
        if(character=='h'){
            this.x=1;
            this.y=0;
        }
        if(character=='j'){
            this.x=0;
            this.y=1;
        }
        if(character=='j'){
            this.x=0;
            this.y=1;
        }
    }

}
